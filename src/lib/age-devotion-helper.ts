// 묵상 데이터에서 매칭되는 키워드 찾기
function findMatchingKeywords(devotional: Devotional): string[] {
  const searchText = `${devotional.theme} ${devotional.passage} ${devotional.meditation} ${devotional.question1} ${devotional.question2} ${devotional.question3}`;
  const matched: string[] = [];
  for (const keyword of Object.keys(themeKeywords)) {
    if (searchText.includes(keyword)) {
      matched.push(keyword);
    }
  }
  return matched;
}

// 시즌에서 매칭되는 기본 콘텐츠 찾기
function findSeasonContent(season: string | null): string | null {
  if (!season) return null;
  for (const key of Object.keys(seasonDefaults)) {
    if (season.includes(key)) return key;
  }
  return null;
}

export function getExtendedAgeContent(
  devotional: Devotional,
  ageGroup: 'children' | 'youth' | 'young_adult' | 'senior'
): AgeExtendedContent {
  const ageKey = ageGroup === 'senior' ? 'age_young_adult' : `age_${ageGroup}`;
  const mainGuide = devotional[ageKey as keyof Devotional] as string || '';

  // 키워드 매칭으로 보충 콘텐츠 선택
  const matchedKeywords = findMatchingKeywords(devotional);
  let supplementary: { tip: string; prayer: string; action: string } | null = null;

  if (matchedKeywords.length > 0) {
    // 가장 첫 번째 매칭 키워드 사용
    supplementary = themeKeywords[matchedKeywords[0]][ageGroup];
  }

  // 키워드 매칭 없으면 시즌 기본값 사용
  if (!supplementary) {
    const seasonKey = findSeasonContent(devotional.season);
    if (seasonKey) {
      supplementary = seasonDefaults[seasonKey][ageGroup];
    }
  }

  // 그래도 없으면 범용 콘텐츠
  if (!supplementary) {
    supplementary = themeKeywords['믿음'][ageGroup];
  }

  return {
    mainGuide: mainGuide,
    applicationTip: supplementary.tip,
    prayerGuide: supplementary.prayer,
    actionStep: supplementary.action,
  };
}
